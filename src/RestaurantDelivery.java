    private void mSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mSaveActionPerformed
     
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Order.txt"))){
            for (Order order : orderList){
                String line = order.getOrderId() + "," + order.getCustomerName() + "," + order.getPhone() + "," + order.getAddress() + "," + order.getFoodItem() + "," + order.price ;
                writer.write(line);
                writer.newLine();
            }
            
            
        }
        catch (IOException ex) {
            System.err.println("Error saving to Order.txt: " + ex.getMessage());
        }
    }//GEN-LAST:event_mSaveActionPerformed


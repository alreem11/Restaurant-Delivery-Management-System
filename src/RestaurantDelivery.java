    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        String orderId = tfID.getText();
        String customerName = tfName.getText();
        String phone = tfPhone.getText();
        String address= tfAddress.getText();
        String foodItem =(String) cmbItems.getSelectedItem();
        
        String[] parts = foodItem.split("-");
        double price = Double.parseDouble(parts[1].trim());
        foodItem = parts[0].trim();
        
        
        
        
        if (orderId.isEmpty() || customerName.isEmpty() || phone.isEmpty() || address.isEmpty() || foodItem.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "All field must be filled", "Input Error" , JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Order newOrder = new Order(orderId, customerName, phone, address, foodItem , price);
        orderList.add(newOrder);
        
        Object[] row = {orderId, customerName,phone,address,foodItem , price};
        tableModel.addRow(row);
        
        
        tfID.setText("");
        tfName.setText("");
        tfPhone.setText("");
        tfAddress.setText("");
        cmbItems.setSelectedIndex(0);
        
        
        lbTotalPrice2.setText("Total Price :" + calculateTotalPrice() + " SR");
    }//GEN-LAST:event_btnAddActionPerformed

    private void mOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mOpenActionPerformed
        
        tfID.setText("");
        tfName.setText("");
        tfPhone.setText("");
        tfAddress.setText("");
        cmbItems.setSelectedIndex(0);
        ((DefaultTableModel) table.getModel()).setRowCount(0);
        orderList.clear();
        
        lbTotalPrice2.setText("Total Price :"+ calculateTotalPrice() + " RS");
        
        
        try (BufferedReader reader = new BufferedReader(new FileReader("Order.txt"))) {
            String line;
            while ((line = reader.readLine()) !=null) {
                String[] data = line.split(",");
                if (data.length == 6){
                    String orderID = data[0];
                    String name = data[1];
                    String phone = data[2];
                    String address = data[3];
                    String foodItem = data[4];
                    double price = Double.parseDouble(data[5]);
                    
                    Order order = new Order(orderID, name, phone, address, foodItem , price);
                    orderList.add(order);
                    tableModel.addRow(new Object[]{orderID, name, phone, address, foodItem, price});
                    
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading file: "+ e.getMessage(), "Read Error", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_mOpenActionPerformed

